// switch 

const enrolment=2;
switch(enrolment) {
    case 1:
        console.log("on control");
        break
    case 2:
        console.log("two control");
        break
    case 3:
        console.log("three control");
        break
    case 4:
        console.log("four control");
        break
    default:
        console.log("default case");
}